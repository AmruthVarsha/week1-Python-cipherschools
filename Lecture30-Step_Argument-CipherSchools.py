# syntax - [start argument : stop argument + 1 : step]
# step - jumps the number of times

print("amruth varsha"[2:5])
print("amruth varsha"[0:5:1])
print("amruth varsha"[0:5:2])
print("amruth varsha"[0:5:3])
print("amruth varsha"[0::2])
print("amruth varsha"[::2])

# backwards step
print("amruth varsha"[5::-1]) # reverse the string
print("amruth varsha"[-1::-1]) #### very imp trick
